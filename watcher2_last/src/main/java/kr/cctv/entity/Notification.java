package kr.cctv.entity;

public class Notification {

	private int noti_no;
	private String noti_img;
	private String noti_name;
	private String noti_time;

	public int getNoti_no() {
		return noti_no;
	}

	public void setNoti_no(int noti_no) {
		this.noti_no = noti_no;
	}

	public String getNoti_img() {
		return noti_img;
	}

	public void setNoti_img(String noti_img) {
		this.noti_img = noti_img;
	}

	public String getNoti_name() {
		return noti_name;
	}

	public void setNoti_name(String noti_name) {
		this.noti_name = noti_name;
	}

	public String getNoti_time() {
		return noti_time;
	}

	public void setNoti_time(String noti_time) {
		this.noti_time = noti_time;
	}

}
